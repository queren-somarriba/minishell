int	ft_isprint(int c)
{
	if ((c >= 32 && c < 127))
		return (16384);
	else
		return (0);
}

int	ft_is_white_space(int c)
{
	if (c == '\f' || c == '\n' || c == '\r'
		|| c == '\t' || c == '\v' || c == ' ')
		return (1);
	return (0);
}
